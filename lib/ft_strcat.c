char	*ft_strcat(char *restrict s1, const char *restrict s2)
{
	int i;
	int end;

	i = 0;
	end = 0;
	while (s1[i] != '\0')
		i++;
	end = i;
	i = 0;
	while (s2[i] != '\0')
	{
		s1[end + i] = s2[i];
		i++;
	}
	s1[end + i] = '\0';
	return (s1);
}
